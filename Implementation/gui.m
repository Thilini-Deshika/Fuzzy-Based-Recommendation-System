function answert = getRecommendation(recVal)

if (recVal < 50) 
    answert =  'Not Recommended';
else
    answert =  'Recommended';
end

function valid = checkFunc(value, handle)
flag = value >= 0 && value <= 100;
if ~flag
   valid = false;
   set(handle, 'String', '0');
else
   valid = true;
end


% --- Executes on button press in GenButton.
function GenButton_Callback(hObject, eventdata, handles)
% hObject    handle to GenButton (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

valid1 = checkFunc(str2double(get(handles.edit1, 'String')),handles.edit1);
valid2 = checkFunc(str2double(get(handles.edit2, 'String')),handles.edit2);
valid3 = checkFunc(str2double(get(handles.edit3, 'String')),handles.edit3);
valid4 = checkFunc(str2double(get(handles.edit4, 'String')),handles.edit4);
valid5 = checkFunc(str2double(get(handles.edit5, 'String')),handles.edit5);
valid6 = checkFunc(str2double(get(handles.edit6, 'String')),handles.edit6);
valid7 = checkFunc(str2double(get(handles.edit7, 'String')),handles.edit7);
valid8 = checkFunc(str2double(get(handles.edit8, 'String')),handles.edit8);

if ~(valid1 && valid2 && valid3 && valid4 && valid5 && valid6 && valid7 && valid8)
    warndlg('Invalid input! Please Enter a value between 0 and 100');
    return 
end


handles.test1.String = '';
handles.test2.String = '';
handles.test3.String = '';
handles.test4.String = '';
