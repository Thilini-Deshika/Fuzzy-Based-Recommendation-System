res = [handles.res1, handles.res2, handles.res3, handles.res4, handles.res5];
count = 1;
%ML and big data
if (handles.checkbox1.Value == true)
    fis = readfis('fis_files/ml');
    input1 = str2num(char(get(handles.edit1,'String')));
    input3 = str2num(char(get(handles.edit3,'String')));
    ml_out = evalfis([input1,input3],fis);
    set(arr(count), 'String', sprintf('%.2f %%', ml_out));
    set(name(count), 'String','Machine Learning and Big Data');
    set(res(count), 'String', getRecommendation(ml_out));
    count = count + 1;
end

