    fis = readfis('fis_files/security');
    input2 = str2num(char(get(handles.edit2,'String')));
    input4 = str2num(char(get(handles.edit4,'String')));
    sec_out = evalfis([input2,input4],fis);
    set(arr(count), 'String', sprintf('%.2f %%', sec_out));
    set(name(count), 'String','Network Security');
    set(res(count), 'String', getRecommendation(sec_out));
    count = count + 1;
end

%Cloud Computing 
if (handles.checkbox3.Value == true)
    fis = readfis('fis_files/cloud');
    input7 = str2num(char(get(handles.edit7,'String')));
    cloud_out = evalfis(input7,fis);
    set(arr(count), 'String', sprintf('%.2f %%', cloud_out));
    set(name(count), 'String','Cloud Computing');
    set(res(count), 'String', getRecommendation(cloud_out));
    count = count + 1;
end

%Embedded Systems    
if (handles.checkbox4.Value == true)
    fis = readfis('fis_files/embedded');
    input5 = str2num(char(get(handles.edit5,'String')));
    input6 = str2num(char(get(handles.edit6,'String')));
    emb_out = evalfis([input5,input6],fis);
    set(arr(count), 'String', sprintf('%.2f %%', emb_out));
    set(name(count), 'String','Embedded Systems');
